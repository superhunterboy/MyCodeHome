            <div class="help-pic help1"></div>
        </li>
        <li class="step third_step">
            <p>
                <font>第二步：</font>在“描述文件”中找到名称为“LINKFUL PARTNER SOFTWARE COMPANY, INC.“的企业级应用，点击进入。</p>
            <div class="help-pic help2"></div>
        </li>
        <li class="step fourth_step">
            <p>
                <font>第三步：</font>进入后，点击蓝色字体 信任“LINKFUL PARTNER SOFTWARE COMPANY, INC.“</p>
            <div class="help-pic help3"></div>
        </li>
        <li class="step fifth_step">
            <p>
                <font>第四步：</font>在弹出的对话框中点击红色字体的“信任“后，游戏客户端不能开启的问题就解决了。</p>
            <div class="help-pic help4"></div>
        </li>
        <li class="step sixth_step">
            <p>
                <font>第五步：</font>按下苹果手机HOME键回到主界面，点击博狼娱乐手机客户端，就可以开启应用了。</p>
            <div class="help-pic help5"></div>
        </li>
    </ul>
