//20170210新轮播
(function () {
    $(function () {
        banners(1,4000,600); //第一个参数是初始时显示2*2+1=5张图片，第二个参数是轮播的间隔时间，第三个参数是图片移动的时间
    });
    function banners(m,t1,t2) {
        var oSlider = $('#J-slider-index-banner .inner .inner'),
            oLi = $('#J-slider-index-banner .inner > .item'),
            widths = oSlider.width(),
            a = m;

        for(var i =0; i < a*2+1; i++){
            oLi.eq(i).clone().appendTo(oSlider);
        }

        var len = $('#J-slider-index-banner .inner > a').length,
            prev = $('#J-slider-index-banner .mask-a'),
            next = $('#J-slider-index-banner .mask-b'),
            onOff = true;
        //设置sliders的宽度
        oSlider.css({'width' : widths * len,'margin-left': -widths * a});
        //当大于2张图片时才能进行轮播
        if (len > 2) {
            var timer = setInterval(auto, t1);
            function auto() {
                m++;
                if (m == len - (2*a-1)) {
                    oSlider.css('marginLeft', -widths);
                    m = 2;
                }
                showSlider(m);
            }
            function showSlider(n) {
                var nowLeft = -n * widths;
                oSlider.stop(true, false).animate({'marginLeft': nowLeft}, t2, function () {
                    onOff = true;
                });
            }
            oSlider.hover(function () {
                clearInterval(timer);
            }, function () {
                timer = setInterval(auto, t1);
            });
            prev.hover(function () {
                clearInterval(timer);
            }, function () {
                timer = setInterval(auto, t1);
            });
            next.hover(function () {
                clearInterval(timer);
            }, function () {
                timer = setInterval(auto, t1);
            });
            prev.click(function () {
                if (onOff) {
                    onOff = false;
                    m--;
                    if (m == a-1) {
                        oSlider.css('marginLeft', -widths * (len - (a+1)));
                        m = len - (a+2);
                    }
                    showSlider(m);
                }
            });
            next.click(function () {
                if (onOff) {
                    onOff = false;
                    auto();
                }
            });
        }
    }
}());

